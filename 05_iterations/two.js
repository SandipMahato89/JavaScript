// while loop

let i = 1;
while (i <= 10) {
    // console.log(`Value of i = ${i}`);
    i = i + 2;
}

let myArray = ["Ram", "Hanuman", "Krishna", "Shiva"]

let idx = 0
while (idx < myArray.length) {
    console.log(`Value is ${myArray[idx]}`);
    idx++;
}

// do-while loop

let score = 11

do {
    console.log(`Score is ${score}`);
    score++
}while(score <= 10)